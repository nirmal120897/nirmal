//program of finding character is vowel or consonant using switch case//

#include<stdio.h>
 void main()
  {
  	  char ch;
  	  printf("enter the character ch=");
  	  scanf("%c",&ch);
  	  switch(ch)
  	  {
  	  	case 'a':
  	  		case'i':
  	  			case 'o':
  	  				case 'u':
  	  					case 'e':
  	  						printf("the character is vowel");
  	  						break;
  	  						default:
  	  							printf("the character is consonant");
  	  							break;
		}
		
 }
